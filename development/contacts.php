                            iconLayout: 'default#image',
                            iconImageHref: 'img/map-pin.png',
                            iconImageSize: [35, 90],
                            iconImageOffset: [-15, -15]
                         }));


                         myMap.geoObjects.add(myCollection);





                             // Масштабируем карту на область видимости геообъекта.
                             myMap.setBounds(bounds, {
