													<p><strong style="color: #0055b0;">Апости́ль *</strong>(фр.&nbsp;<em>Apostille</em>)&nbsp;— международная стандартизированная форма заполнения сведений о законности документа для предъявления на территории стран, признающих такую форму легализации.</p>
													<p>Апостиль – это специальный штамп, который в соответствии с Гаагской конвенцией 1961 года ставится на официальных документах государств-участников Конвенции с целью освободить такие документы от необходимости дипломатической или консульской легализации.<br>
